/**
 * Max-width preset for centered / constrained modal panels.
 *
 * Used by {@link import('@/modules/types/support/modals/ModalProps.ts').ModalProps ModalProps}.size for responsive shell sizing.
 *
 * Allowed values:
 * - `sm` — narrow dialog.
 * - `md` — default medium width.
 * - `lg` — wide content.
 * - `xl` — extra wide.
 * - `2xl` — very wide layouts.
 * - `full` — near viewport width.
 */
export type ModalSize = 'sm' | 'md' | 'lg' | 'xl' | '2xl' | 'full';
